/*1. Fazer um programa que pergunta um valor em metros e imprime o 
correspondente em decımetros, centımetros e milımetros.
*/

#include <stdio.h>
#include <string.h>
#include <math.h>

int main(void) {

    int m;

    printf("Digite o valor em metros: ");
    scanf("%d", &m);

    printf("Decimetro: %d\n", m * 10);
    printf("Centimetro: %d\n", m * 100);
    printf("Milimetro %d\n", m * 1000);






  return 0;
}


